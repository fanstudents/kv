export default function BrandLogo({
  brand,
  name,
  color,
  size = 44,
  bare = false,
}: {
  brand: string;
  name: string;
  color: string;
  size?: number;
  /** 省略外框卡片（白底＋邊框），直接輸出裸 SVG——用於已有自己容器（例如流程節點圓圈）的情境 */
  bare?: boolean;
}) {
  const b = BRANDS[brand];
  const radius = Math.round(size * 0.28);

  if (b && bare) {
    return (
      <svg viewBox={b.viewBox} width={size} height={size} aria-hidden="true">
        {b.body}
      </svg>
    );
  }

  if (b) {
    const inner = b.fullBleed ? size : Math.round(size * 0.6);
    return (
      <span
        className={`flex shrink-0 items-center justify-center overflow-hidden ${
          b.fullBleed ? "" : "border border-neutral-200 bg-white dark:border-neutral-700"
        }`}
        style={{ width: size, height: size, borderRadius: radius }}
      >
        <svg viewBox={b.viewBox} width={inner} height={inner} aria-hidden="true">
          {b.body}
        </svg>
      </span>
    );
  }

  // 未收錄品牌：品牌色字首色塊
  return (
    <span
      className="flex shrink-0 items-center justify-center font-bold text-white"
      style={{
        width: size,
        height: size,
        borderRadius: radius,
        backgroundColor: color,
        fontSize: Math.round(size * 0.42),
      }}
      aria-hidden="true"
    >
      {name.trim().charAt(0).toUpperCase() || "?"}
    </span>
  );
}
